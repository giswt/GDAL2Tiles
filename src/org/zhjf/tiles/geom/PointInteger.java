public class PointInteger {
    private int x;
    private int y;

    public PointInteger(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public PointInteger() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
